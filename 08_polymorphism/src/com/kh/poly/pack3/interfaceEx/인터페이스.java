package com.kh.poly.pack3.interfaceEx;
//inferface : 접점 (클래스간의 접점을 만드는 용도)
public interface 인터페이스 {
    /*
    인터페이스는 상수형 필드(public static final)만 작성 가능
    작성하는 모든 필드가 public static final 구문을 작성하지 않아도
    암묵적으로 인식됨

    작성하는 모든 변수명이 상수형으로 인신

     */

    public String ABC = "220";
    //abc -> 상수형 모양 중간에 static final 이 생략되어있음. 대문자로 작성해야함
}
